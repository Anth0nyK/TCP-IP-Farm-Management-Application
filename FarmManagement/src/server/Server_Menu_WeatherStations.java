    private void updateWeatherStations() {
        this.weatherStations = FileHandler.getInstance().getAllWeatherStations();
        DefaultTableModel model = (DefaultTableModel) this.TBLWeatherStations.getModel();
        model.setNumRows(0);
        for (Map.Entry<Integer, List<String>> weatherStation : this.weatherStations.entrySet()) {
            String activate = (weatherStation.getValue().get(1).equals("1")) ? "ACTIVATE" : "DEACTIVATE";
            model.addRow(new Object[]{weatherStation.getValue().get(0), weatherStation.getValue().get(2), 
                weatherStation.getValue().get(3), weatherStation.getValue().get(4), activate, "Edit"});
        }
    }

