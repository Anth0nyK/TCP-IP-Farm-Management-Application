    private void TBLWeatherStationsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TBLWeatherStationsMouseClicked
        JTable tableSource = (JTable)evt.getSource();
        int row = tableSource.rowAtPoint(evt.getPoint());
        int column = tableSource.columnAtPoint(evt.getPoint());

        int weatherStationId = -1;
        switch (column) {
            case 4:
                weatherStationId = Integer.parseInt((String)tableSource.getModel().getValueAt(row, 0));
                String action = (String)tableSource.getModel().getValueAt(row, 4);
                activateWeatherStation(weatherStationId, (action != "ACTIVATE"));
                break;
            case 5:
                PNLWeatherStationEdit.setVisible(true);
                this.validate();
                weatherStationId = Integer.parseInt((String)tableSource.getModel().getValueAt(row, 0));
                updatePNLUserData(weatherStationId);
                break;
            default:
                PNLWeatherStationEdit.setVisible(false);
                this.validate();
                break;
        }

        // JOptionPane.showMessageDialog(null, s + " " + (column==3));
    }//GEN-LAST:event_TBLWeatherStationsMouseClicked
    private void activateWeatherStation(int weatherStationId, boolean action) {
        FileHandler.getInstance().changeWeatherStationActiveStatus(weatherStationId, action);
        updateWeatherStations();
    }
    
    private void updatePNLUserData(int weatherStationId) {
        List<String> currentWeatherStation = weatherStations.get(weatherStationId);
        this.TXTWeatherStationId.setText(currentWeatherStation.get(0));
        this.TXTGPSLocation.setText(currentWeatherStation.get(2));
        this.TXTHectares.setText(currentWeatherStation.get(3));
        this.TXTUserFor.setText(currentWeatherStation.get(4));
    }
