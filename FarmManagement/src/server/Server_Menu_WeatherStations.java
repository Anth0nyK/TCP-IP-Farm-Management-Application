    private void updateCurrentWeatherStationData() {
        int weatherStationId = Integer.parseInt(this.TXTWeatherStationId.getText());
        String GPSLocation = this.TXTGPSLocation.getText();
        String hectares = this.TXTHectares.getText();
        String userFor = this.TXTUserFor.getText();
        
        if (GPSLocation.isEmpty() || hectares.isEmpty() || userFor.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please don't leave any field empty.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        FileHandler.getInstance().updateWeatherStationDate(weatherStationId, GPSLocation, hectares, userFor);
        updateWeatherStations();
        JOptionPane.showMessageDialog(this, "User data sucessfully updated!", "Success", JOptionPane.INFORMATION_MESSAGE);
    }

